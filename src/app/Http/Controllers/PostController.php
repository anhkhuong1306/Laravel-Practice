    {
        $search = $request->input('search');
        $category = $request->input('category');
        $author = $request->input('author');
        return view(
            'posts.index',
            [
                'posts' =>  Post::latest()->filter(
                    [
                        'search' => $search,
                        'category' => $category,
                        'author' => $author,
                    ],
                )->paginate(10)->withQueryString()
            ]
        );
    }

